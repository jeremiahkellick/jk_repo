#include <stdint.h>
#include <stdio.h>

char bits[] = "010100100010101000010101001101001011001110110011101101010010101110001010000000001100"
              "101000000001010100000010011100001011011000000000000000000000001000011000000100000111"
              "010101001111110010000010111010110101011000001000000001010000110000110110111100100101"
              "0100111101110000111000000010101100001011111000110110011000101111";

int main(void)
{
    uint64_t length = sizeof(bits) - 1;
    uint64_t cursor = 0;
    while (cursor < length) {
        uint8_t byte = 0;
        for (uint8_t i = 0; cursor < length && i < 8; i++) {
            byte |= (bits[cursor++] == '1') << i;
        }
        printf("0x%02x, ", (uint32_t)byte);
    }

    return 0;
}
